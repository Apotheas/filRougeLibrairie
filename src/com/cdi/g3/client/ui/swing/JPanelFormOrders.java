        if ((jTextDiscount.getText() != null) && (Integer.parseInt(jTextDiscount.getText()) != 0)) {
            prixHT = prixHT - ((prixHT * Integer.parseInt(jTextDiscount.getText())) / 100);

        }

        float prixTCC = prixHT + (prixHT * codeTVA.getRateCodeTva()) / 100;
