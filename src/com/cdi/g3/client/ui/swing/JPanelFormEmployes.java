    private void jButtonEmployeSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEmployeSearchActionPerformed
        try {                                                  
            Employe employe= null;
            try {
                CompanyService employeService = new CompanyService();
                employe = employeService.findEmploye(jTextUpdateEmploye.getText());
            } catch (FinderException ex) {
                Logger.getLogger(JPanelFormEmployes.class.getName()).log(Level.SEVERE, null, ex);
            } catch (CheckException ex) {
                Logger.getLogger(JPanelFormEmployes.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
            jTextLogin.setText(employe.getId());
            jTextEmail.setText(String.valueOf(employe.getEmailEmploye()));
            jTextFirstName.setText(employe.getFirstNameEmploye());
            jTextLastName.setText(employe.getFirstNameEmploye());
            
            
            PublishingService service = new PublishingService();            
            Collection<Appreciation> listAppreciation = new ArrayList();
            
            listAppreciation = service.findAppreciationByISBN(employe.getId());
           
            
            Appreciation appreciation = (Appreciation)service.findEditorByISBN(employe.getId());
            jTextEditor.setText(appreciation.getNameEditor());
        } catch (ObjectNotFoundException ex) {
            Logger.getLogger(JPanelFormEmployes.class.getName()).log(Level.SEVERE, null, ex);
        }
          
        
                                                     
    }//GEN-LAST:event_jButtonEmployeSearchActionPerformed

